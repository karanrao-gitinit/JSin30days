// #1 Iterate 0 to 10 using for loop, do the same using while and do while loop

// NOTE: Run both the codes separately

// Loop
for (let i = 0; i <= 10; i++) {
    console.log(i);
}

// While loop
let i = 0
while (i <= 10) {
    console.log(i);
    i++;
}

// do-while loop
do {
    console.log(i);
    i++
} while (i <= 10);


